br(),

"Este modelo se construyó utilizando la metodología IPSIM
(Injury Profile SIMulator)1, que tiene la ventaja de representar de manera
simplificada las complejas relaciones que existen dentro de los
agroecosistemas y permite una fácil apropiación de la herramienta
por parte de los actores del sector. IPSIM Roya combina variables
cualitativas ordinales que describen las interacciones entre el clima,
el patosistema y el manejo de los cultivos que afectan el riesgo de crecimiento
de la roya cada mes.",

br(),
br(),

"En primer lugar, este modelo fue evaluado socialmente en talleres nacionales
en Centroamérica y República Dominicana. En segundo lugar, se evaluó numéricamente
sobre los datos de vigilancia de la roya a lo largo de cuatro países y tres años.
Como el modelo se ajustaba bien a los datos, actualmente se utiliza para producir
alertas mensuales en Honduras a través de Pergamino2.",
          
